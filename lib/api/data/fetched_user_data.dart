abstract class UserData {
  static int? id;
  static String? userName;
  static String? firstName;
  static String? lastName;
  static String? email;
  static String? accessToken;
  static String? apiToken;
  static String? phone;
  static String? gender;
  static String? altPhone;
  static String? age;
  static String? image;
  static String? status;
  static String? country;
  static String? city;
  static String? timezone;
  static String? timezoneDiff;
  static String? paymentMethod;
  static String? invoiceType;
  static int? invoicePaymentType;
  static String? currency;
  static String? hourRate;
  static String? totalHours;
  static String? deletedAt;
  static String? createdAt;
  static String? updatedAt;
  static String? zoomEmail;
  static String? zoomPassword;
  static String? zoomLink;
  static String? groupId;
  static bool? isTeacher;

  static void setUserData(Map data, bool isTeacher) {
    userName = data['user']['user_name'];
    apiToken = data['user']['api_token'];
    city = data['user']['city'];
    id = data['user']['id'];
    firstName = data['user']['first_name'];
    lastName = data['user']['last_name'];
    email = data['user']['email'];
    phone = data['user']['phone'];
    gender = data['user']['gender'];
    image = data['user']['image'];
    status = data['user']['status'];
    country = data['user']['country'];
    timezone = data['user']['timezone'];
    timezoneDiff = data['user']['timezone_diff'];
    paymentMethod = data['user']['payment_method'];
    invoiceType = data['user']['invoid_type'];
    invoicePaymentType = data['user']['invoice_payment_type'];
    currency = data['user']['currency'];
    hourRate = data['user']['hour_rate'];
    totalHours = data['user']['total_hour'];
    altPhone = data['user']['alt_phone'];
    updatedAt = data['user']['updated_at'];
    deletedAt = data['user']['deleted_at'];
    createdAt = data['user']['created_at'];
    zoomEmail = data['user']['zoom_email'];
    zoomLink = data['user']['zoom_meeting_link'];
    zoomPassword = data['user']['zoom_password'];
    groupId = data['user']['group_id'];
    accessToken = data['access_token'];
    UserData.isTeacher = isTeacher;
  }

  static void removeData() {
    userName = null;
    apiToken = null;
    city = null;
    id = null;
    firstName = null;
    lastName = null;
    email = null;
    phone = null;
    gender = null;
    image = null;
    status = null;
    country = null;
    timezone = null;
    timezoneDiff = null;
    paymentMethod = null;
    invoiceType = null;
    invoicePaymentType = null;
    currency = null;
    hourRate = null;
    totalHours = null;
    altPhone = null;
    updatedAt = null;
    deletedAt = null;
    createdAt = null;
    zoomEmail = null;
    zoomLink = null;
    zoomPassword = null;
    groupId = null;
    accessToken = null;
  }
}
